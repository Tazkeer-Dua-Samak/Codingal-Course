print("num1 ^ num2 are: ", num1 ^ num2)
print("num1 << num2 are: ", num1 << num2)
print("num1 >> num2 are: ", num1 >> num2)
print("~num1 is: ", ~num1)
print("~num2 is: ", ~num2)